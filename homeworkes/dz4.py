# Этот код создает массив чисел с помощью библиотеки numpy, выполняет операции сложения и выводит результаты.

import numpy as np

# Создаем массив из чисел от 0 до 9
array = np.arange(10)

# Увеличиваем каждое значение в массиве на 5
modified_array = array + 5

# Выводим оригинальный и модифицированный массивы
print("Оригинальный массив:", array)
print("Массив после модификации:", modified_array)
